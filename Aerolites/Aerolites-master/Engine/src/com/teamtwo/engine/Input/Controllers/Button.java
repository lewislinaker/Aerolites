package com.teamtwo.engine.Input.Controllers;

/**
 * An enumeration which represents the supported Controller buttons<br>
 *     These are in Xbox format
 */
public enum Button {

    /** The A or Cross button */
    A,
    /** The B or Circle button */
    B,
    /** The X or Square button */
    X,
    /** The Y or Triangle button */
    Y,

    /** The LB or L1 button */
    LB,
    /** The L3 button */
    L3,

    /** The RB or R1 button */
    RB,
    /** The R3 button */
    R3,

    /** The Start or Options button */
    Start,
    /** The Back, Select or Share button */
    Back,
    /** The Xbox or PlayStation button */
    Xbox,

    /** Up on the DPad */
    DPad_Up,
    /** Down on the DPad */
    DPad_Down,
    /** Left on the DPad */
    DPad_Left,
    /** Right on the DPad */
    DPad_Right

}
